package LogicHandling;

public class Coordinates {
    private final int x_cur;
    private final int y_cur;


    public Coordinates(int x_cur, int y_cur) {
        this.x_cur = x_cur;
        this.y_cur = y_cur;
    }

    public int getX_cur() {
        return x_cur;
    }

    public int getY_cur() {
        return y_cur;
    }
}
